

class Car
{
  public:

   int id;
   double x;
   double y;
   double vx;
   double vy;
   double s;
   double d;

   Car(int id, double x, double y, double vx, double vy, double s, double d)
   {
       this->id= id;
       this->x = x;
       this->y = y;
       this->vx = vx;
       this->vy = vy;
       this->s = s;
       this->d= d;
   }

};
